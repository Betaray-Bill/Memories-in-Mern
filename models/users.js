const mongoose = require('mongoose')

const userSchema = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    id: { type: String }
})

const userMessage = mongoose.model('userMessage', userSchema)

module.exports = userMessage